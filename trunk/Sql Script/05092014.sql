				SET @RETURN_VALUE = 0;
			END
			ELSE
			BEGIN
				SET @MA_SAN_PHAM = 0;
				SET @SO_LUONG = 0;
				
				DECLARE DETAIL_CURSOR_EXPORT_TO_PROVIDER CURSOR FOR
				SELECT 
					TRA_HANG_NCC_CHI_TIET.MA_SAN_PHAM,
					TRA_HANG_NCC_CHI_TIET.SO_LUONG,
					TRA_HANG_NCC_CHI_TIET.DON_GIA,
